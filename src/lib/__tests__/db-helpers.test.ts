import { describe, it, expect, vi, beforeEach } from 'vitest'

// Use vi.hoisted() so mock variables are available inside vi.mock() factories
const {
  mockBroadcast,
  mockActivitiesCreate,
  mockNotificationsCreate,
  mockAgentsFindFirst,
  mockAgentsUpdateMany,
} = vi.hoisted(() => {
  const mockBroadcast = vi.fn()
  const mockActivitiesCreate = vi.fn(async () => ({ id: 1 }))
  const mockNotificationsCreate = vi.fn(async () => ({ id: 1 }))
  const mockAgentsFindFirst = vi.fn(async () => ({ id: 42 }))
  const mockAgentsUpdateMany = vi.fn(async () => ({ count: 1 }))
  return {
    mockBroadcast,
    mockActivitiesCreate,
    mockNotificationsCreate,
    mockAgentsFindFirst,
    mockAgentsUpdateMany,
  }
})

// Mock better-sqlite3 native module to avoid needing compiled bindings
